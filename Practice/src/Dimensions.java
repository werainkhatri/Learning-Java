public class Dimensions {
    private int width;
    private int depth;
    private int length;

    public Dimensions(int width, int depth, int length) {
        this.width = width;
        this.depth = depth;
        this.length = length;
    }

    public int getWidth() {
        return width;
    }

    public int getDepth() {
        return depth;
    }

    public int getLength() {
        return length;
    }
}
